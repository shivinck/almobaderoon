                    <h1 class="font-lora font-40 lineh-44 weight-600 color-29 margin-b24">Client Background</h1>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">Our client is a mid-sized general trading company based in Dubai, established in 2017, with annual revenues of approximately AED 12 million. The company deals primarily in electronics and consumer goods, importing from Asia and distributing across the UAE and GCC markets. They had been VAT-registered since January 2018 and employed a freelance accountant to handle their tax filings.</p>

                    <h1 class="font-lora font-40 lineh-44 weight-600 color-29 margin-b24">The Challenge</h1>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">In March 2023, the company received a tax assessment notice from the Federal Tax Authority (FTA) totaling AED 180,000 in penalties. The breakdown included:</p>
                    <ul class="margin-b24" style="padding-left: 20px;">
                        <li class="font-ks font-16 lineh-30 weight-400 color-30">AED 65,000 in late filing penalties across 6 VAT return periods (2021–2022)</li>
                        <li class="font-ks font-16 lineh-30 weight-400 color-30">AED 48,000 in penalties for incorrect input tax recovery on non-eligible expenses</li>
                        <li class="font-ks font-16 lineh-30 weight-400 color-30">AED 42,000 in administrative penalties for failure to maintain proper tax records</li>
                        <li class="font-ks font-16 lineh-30 weight-400 color-30">AED 25,000 in penalties for inaccurate tax return information</li>
                    </ul>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">The company's previous accountant had filed returns late on multiple occasions, claimed input tax on entertainment and personal vehicle expenses, and failed to maintain the required documentation for several transactions. The company was facing a significant financial burden and potential disruption to their operations.</p>

                    <h1 class="font-lora font-40 lineh-44 weight-600 color-29 margin-b24">Our Approach</h1>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">Al Mobaderoon was engaged in April 2023, just 15 days before the reconsideration deadline. Our team took immediate action:</p>

                    <h2 class="font-lora font-28 lineh-32 weight-600 color-29 margin-b14">Phase 1: Comprehensive Review (Days 1–5)</h2>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">We conducted a thorough review of all VAT returns filed between 2021 and 2022, cross-referencing them with the company's accounting records, bank statements, and purchase invoices. We identified that 4 out of 6 late filings were due to the accountant's negligence rather than the company's intent, with email evidence showing the company had provided all documents on time.</p>

                    <h2 class="font-lora font-28 lineh-32 weight-600 color-29 margin-b14">Phase 2: Evidence Compilation (Days 5–10)</h2>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">We compiled a comprehensive evidence package including timestamped communications between the company and their previous accountant, corrected VAT calculations, proper categorization of input tax claims, and reconstructed records for transactions that lacked documentation. We also obtained a statement from the previous accountant acknowledging the filing delays.</p>

                    <h2 class="font-lora font-28 lineh-32 weight-600 color-29 margin-b14">Phase 3: Reconsideration Submission (Days 10–14)</h2>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">We prepared a detailed reconsideration request citing relevant FTA guidelines and Cabinet Decision provisions, clearly demonstrating which penalties resulted from third-party errors versus genuine oversights. Our submission included voluntary disclosures for the periods with incorrect input tax claims, showing the company's good faith and willingness to comply.</p>

                    <h1 class="font-lora font-40 lineh-44 weight-600 color-29 margin-b24">Results & Impact</h1>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">After a 45-day review period, the FTA issued their decision in June 2023:</p>
                    <ul class="margin-b24" style="padding-left: 20px;">
                        <li class="font-ks font-16 lineh-30 weight-400 color-30"><strong>75% reduction in total penalties</strong> — from AED 180,000 to AED 45,000</li>
                        <li class="font-ks font-16 lineh-30 weight-400 color-30">Late filing penalties reduced to AED 20,000 (4 periods waived entirely)</li>
                        <li class="font-ks font-16 lineh-30 weight-400 color-30">Administrative penalties fully waived based on corrected record-keeping</li>
                        <li class="font-ks font-16 lineh-30 weight-400 color-30">Input tax penalties reduced to AED 25,000 with voluntary disclosure credit applied</li>
                    </ul>
                    <p class="font-ks font-16 lineh-26 weight-400 color-30 margin-b24">The company saved AED 135,000 in penalties. Additionally, Al Mobaderoon restructured their bookkeeping processes and implemented a VAT compliance calendar to prevent future issues. The company has maintained a clean compliance record since our engagement.</p>
